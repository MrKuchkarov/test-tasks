function countHandshakes(people) {
    // Инициализирую переменную для подсчета рукопожатий
    let handshakes = 0;
    // Цикл проходит от второго человека (i=2) до общего числа людей
    for (let i = 2; i <= people; i++) {
        // Количество рукопожатий для каждого человека (i-1) добавляется к общему количеству
        handshakes += i - 1;
    }
    // Возвращаю общее количество рукопожатий
    return handshakes;
}

const totalPeople = 10;
const totalHandshakes = countHandshakes(totalPeople);
console.log("Количество рукопожатий для " + totalPeople + " человек: " + totalHandshakes);

// Количество рукопожатий можно выразить с помощью математической формулы: n × (n-1)/2, где n - количество людей.
// Для 10 человек подсчитаем количество рукопожатий:
// 10 × (10-1)/2 = 10 × 9/2 = 45
// Таким образом, для 10 человек количество рукопожатий составит 45.